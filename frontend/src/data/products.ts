// Helper function to get product by ID
export const getProductById = (id: number): Product | undefined => {
  return allProducts.find(product => product.id === id);
};

// Helper function to get related products (same category, excluding current product)
export const getRelatedProducts = (productId: number, limit: number = 4): Product[] => {
  const currentProduct = getProductById(productId);
  if (!currentProduct) return [];
  
  return allProducts
    .filter(p => p.category === currentProduct.category && p.id !== productId)
    .slice(0, limit);
};

// Helper function to get products by category
export const getProductsByCategory = (category: string): Product[] => {
  return allProducts.filter(product => product.category === category);
};

// Helper function to get featured products
export const getFeaturedProducts = (limit?: number): Product[] => {
  const featured = allProducts.filter(product => product.isFeatured);
  return limit ? featured.slice(0, limit) : featured;
};

// Helper function to get new products
export const getNewProducts = (limit?: number): Product[] => {
  const newProducts = allProducts.filter(product => product.isNew);
  return limit ? newProducts.slice(0, limit) : newProducts;
};
